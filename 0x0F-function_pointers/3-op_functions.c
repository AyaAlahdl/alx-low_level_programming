#include "3-calc.h"

/**
 * op_add - add two int
 * @a: the first int
 * @b: the second int
 *
 * Return: the sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtract two int
 * @a: the first int
 * @b: the second int
 *
 * Return: the subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - divid two int
 * @a: the first int
 * @b: the second int
 *
 * Return: the product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two int
 * @a: the first int
 * @b: the second int
 *
 * Return: the quotient
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulo two int
 * @a: the first int
 * @b: the second int
 *
 * Return: the remandir
 */

int op_mod(int a, int b)
{
	return (a % b);
}

